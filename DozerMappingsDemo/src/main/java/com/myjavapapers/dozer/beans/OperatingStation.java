package com.myjavapapers.dozer.beans;

public class OperatingStation {

	private int stationId;
	private String circ7;
	private int tcsYardNumber;
	private String stationName;
	private String stateAbbreviation;
	private String countryAbbreviation;

	public int getStationId() {
		return stationId;
	}

	public void setStationId(int stationId) {
		this.stationId = stationId;
	}

	public String getCirc7() {
		return circ7;
	}

	public void setCirc7(String circ7) {
		this.circ7 = circ7;
	}

	public int getTcsYardNumber() {
		return tcsYardNumber;
	}

	public void setTcsYardNumber(int tcsYardNumber) {
		this.tcsYardNumber = tcsYardNumber;
	}

	public String getStationName() {
		return stationName;
	}

	public void setStationName(String stationName) {
		this.stationName = stationName;
	}

	public String getStateAbbreviation() {
		return stateAbbreviation;
	}

	public void setStateAbbreviation(String stateAbbreviation) {
		this.stateAbbreviation = stateAbbreviation;
	}

	public String getCountryAbbreviation() {
		return countryAbbreviation;
	}

	public void setCountryAbbreviation(String countryAbbreviation) {
		this.countryAbbreviation = countryAbbreviation;
	}

}
